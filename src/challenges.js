function compareTrue(animal1, animal2) {
  let result;

  if (animal1 === 'girafa' && animal2 === 'elefante') {
    result = true;
  } else if (animal1 === 'elefante' && animal2 === 'girafa') {
    result = true;
  } else {
    result = false;
  }
  return result;
}

